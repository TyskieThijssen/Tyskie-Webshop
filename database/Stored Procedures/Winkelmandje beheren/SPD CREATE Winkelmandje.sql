USE TyskieWebshop
GO

/* Stored Procedure CREATE Winkelmandje Beheren */
CREATE PROCEDURE spd_createWinkelmandje
	@afrekenCode	VARCHAR(10),
	@gebruikersnaam	VARCHAR(50)
AS
	SET NOCOUNT, XACT_ABORT ON
	DECLARE @TranCounter INT;
	SET @TranCounter = @@TRANCOUNT;
	IF @TranCounter > 0
		SAVE TRANSACTION ProcedureSave;
	ELSE
		BEGIN TRANSACTION;
BEGIN TRY
		INSERT INTO Winkelmandje VALUES (@afrekenCode, @gebruikersnaam)
		IF @TranCounter = 0 AND XACT_STATE() = 1
			COMMIT TRANSACTION;
END TRY
BEGIN CATCH
		IF @TranCounter = 0 
			BEGIN
				IF XACT_STATE() = 1 ROLLBACK TRANSACTION;
			END;
		ELSE
			BEGIN
				IF XACT_STATE() <> -1 ROLLBACK TRANSACTION ProcedureSave;
			END;	
		THROW
END CATCH
GO